    ; Crash: Jagged Slide Down (Simulates Noise)
    ; 1. Calculate base pitch (Lowering divisor = Higher pitch)
    ; We want Start=High Pitch, End=Low Pitch.
    ; So Divisor must go from Low -> High.
   
    mov bx, 35          ; Max duration
    sub bx, [sfx_timer] ; BX counts UP (0 to 30)
   
    mov ax, bx
    shl ax, 6           ; Scale up (0 to 1920)
    add ax, 800         ; Base Pitch (High start)

