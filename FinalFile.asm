; Input: AX = Frequency Divisor
set_freq:
    push ax         ; Save AX (Caller expects it preserved)
    push cx         ; Save CX (We will use it as temporary storage)

    mov cx, ax      ; Copy the frequency from AX to CX

    ; 1. Send Command to PIT (Port 0x43)
    mov al, 0xB6    ; Command: Mode 3, Channel 2, Read/Write LSB then MSB
    out 0x43, al

    ; 2. Send Low Byte (Port 0x42)
    mov al, cl      ; Move Low Byte from CX to AL
    out 0x42, al

    ; 3. Send High Byte (Port 0x42)
    mov al, ch      ; Move High Byte from CX to AL
    out 0x42, al

    call speaker_on ; Ensure speaker is enabled

    pop cx          ; Restore CX
    pop ax          ; Restore AX
    ret


